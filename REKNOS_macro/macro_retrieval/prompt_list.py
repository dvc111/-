# -*- coding: utf-8 -*-
"""
提示词模板。

设计思想继承自原 REKNOS 项目的 prompt_list.py：
- 用一个 few-shot 示例引导 LLM 输出结构化、可用正则解析的结果；
- 关系 / 超关系打分统一采用 "{标签 (Score: 0.x)}" 的输出格式，
  这样可以复用原项目 clean_relations() 的解析思路（见 utils_local.py）。
"""

# ---------------------------------------------------------------------------
# 1. 实体链接：从问题中抽取候选 mention（不依赖具体 KG，只做提及抽取）
# ---------------------------------------------------------------------------
ENTITY_MENTION_EXTRACT_PROMPT = """请从下面的问题中抽取出所有可能是知识图谱实体的“提及”（mention），\
例如药物名、疾病名、症状名等专有名词。只输出提及本身，每行一个，不要输出多余解释。

问题: 阿司匹林和布洛芬同时服用会有什么风险？
提及:
阿司匹林
布洛芬

问题: {question}
提及:
"""

# ---------------------------------------------------------------------------
# 2. 超关系语义评分：在“超关系”粒度上打分，而不是原版对具体 relation 打分
#    输出格式沿用原版 extract_relation_prompt 的 "{标签 (Score: 0.x)}" 风格
# ---------------------------------------------------------------------------
HYPER_RELATION_SCORE_PROMPT = """你是一个知识图谱推理助手。给定一个问题和若干候选“超关系”\
（超关系是一组语义相近的具体关系的集合，代表某一类知识领域），\
请为每一个候选超关系打分，分数范围 0 到 1，分数越高表示该超关系对回答问题越重要。\
请按下面的格式输出，每个超关系一行：
{{超关系标签 (Score: 0.x)}}: 简要理由

问题: 阿司匹林和布洛芬同时服用会有什么风险？
候选超关系:
1. 药物相互作用
2. 禁忌症
3. 适应症
4. 药物分类
A:
{{药物相互作用 (Score: 0.9)}}: 两种药物同时服用最直接相关的是它们之间的相互作用。
{{禁忌症 (Score: 0.6)}}: 同时服用可能触发某些禁忌症或不良反应。
{{适应症 (Score: 0.1)}}: 适应症与“同时服用的风险”关系较弱。
{{药物分类 (Score: 0.1)}}: 药物分类本身不直接说明风险。

问题: {question}
候选超关系:
{hyper_relation_list}
A:
"""


def build_hyper_relation_prompt(question: str, hyper_relations: list) -> str:
    """
    组装超关系打分 prompt。
    hyper_relations: [{"hyper_relation_id":..., "label":...}, ...]
    """
    lines = [
        "{}. {}".format(i + 1, hr["label"]) for i, hr in enumerate(hyper_relations)
    ]
    return HYPER_RELATION_SCORE_PROMPT.format(
        question=question, hyper_relation_list="\n".join(lines)
    )
